package Lesson7;

public class CircularShapes {


    private int radiusOfCircle;
    private int heightOfCylinder;
    private int lengthOfBigHalfAxis;
    private int lengthOfSmallHalfAxis;


    public void setRadiusOfCircle(int radiusOfCircle) {
        this.radiusOfCircle = radiusOfCircle;
    }

    public void setHeightOfCylinder(int heightOfCylinder) {
        this.heightOfCylinder = heightOfCylinder;
    }

    public void setLengthOfBigHalfAxis(int lengthOfBigHalfAxis) {
        this.lengthOfBigHalfAxis = lengthOfBigHalfAxis;
    }

    public void setLengthOfSmallHalfAxis(int lengthOfSmallHalfAxis) {
        this.lengthOfSmallHalfAxis = lengthOfSmallHalfAxis;
    }
}
